    public void consultar() {
        try {
            DAOVoluntario objvoluntarioDAO = new DAOVoluntario();
            DefaultTableModel model = (DefaultTableModel) JTBvoluntario.getModel();
            ArrayList<Voluntario> lista = objvoluntarioDAO.Display();
            model.setRowCount(0);
            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getIdvoluntario(),
                    lista.get(num).getNome(),
                    lista.get(num).getCpf(),
                    lista.get(num).getTelefone(),
                    lista.get(num).getEmail()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }

