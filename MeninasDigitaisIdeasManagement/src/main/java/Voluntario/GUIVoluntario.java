    public Voluntario getVoluntario() {
        Voluntario v = new Voluntario();
        v.setIdvoluntario(Integer.parseInt(JTFidvoluntario.getText()));
        v.setNome(JTFnome.getText());
        v.setCpf(Long.parseLong(JTFcpf.getText()));
        v.setTelefone(Long.parseLong(JTFtelefone.getText()));
        v.setEmail(JTFemail.getText());
        v.setSenha(JTFsenha.getText());
        return v;
    }

    public int getID() {
        return Integer.parseInt(JTFidvoluntario.getText());
    }
    
